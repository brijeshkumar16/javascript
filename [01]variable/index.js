// We have let,const and var for creating a variable
// let and const are block scope
// var is function scope
// Avoid using the var
// const mean you can't change the variable value
// let mean you can change the variable value

const userName = 'Brijeshkumar Yadav';
console.log(userName);

let age = 12;
console.log(age);

age = 15;
console.log(age);
